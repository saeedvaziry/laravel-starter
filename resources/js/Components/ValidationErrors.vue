<template>
    <div v-if="hasErrors" class="bg-red-50 border border-red-300 rounded-md">
        <ul class="p-4 list-disc list-inside text-sm text-red-600">
            <li v-for="(error, key) in errors" :key="key">{{ error }}</li>
        </ul>
    </div>
</template>

<script>
    export default {
        name: 'VValidationErrors',

        computed: {
            errors() {
                return this.$page.props.errors
            },

            hasErrors() {
                return Object.keys(this.errors).length > 0;
            },
        }
    }
</script>
